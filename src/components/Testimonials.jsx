const Stars = ({ count }) => (
  <div className={styles.stars}>
    {'★'.repeat(count)}{'☆'.repeat(5 - count)}
  </div>
)

export default function Testimonials() {
  const trackRef = useRef(null)
  const [page, setPage] = useState(0)

  const total = REVIEWS.length
  const perPage = 3
  const maxPage = Math.ceil(total / perPage) - 1

  const scrollTo = (p) => {
    setPage(p)
    if (trackRef.current) {
      const cardW = trackRef.current.querySelector(`.${styles.card}`)?.offsetWidth ?? 0
      const gap = 24
      trackRef.current.scrollTo({ left: p * perPage * (cardW + gap), behavior: 'smooth' })
    }
  }

  return (
    <section className={`section ${styles.section}`} id="testimonials">
      <WalkingHorse {...WALKING_HORSE_BY_SECTION.testimonials} />
      <div className="container">
        <h2 className="section-title">
          Что говорят <span className="highlight">наши гости</span>
        </h2>
        <p className="section-subtitle">
          Отзывы тех, кто уже побывал в КСК «Мечта» и навсегда полюбил верховую езду
        </p>

        <div className={styles.sliderWrap}>
          <div className={styles.track} ref={trackRef}>
            {REVIEWS.map((r, i) => (
              <div key={i} className={styles.card}>
                <Stars count={r.stars} />
                <p className={styles.text}>«{r.text}»</p>
                <div className={styles.author}>
                  <div className={styles.avatar}>{r.avatar}</div>
                  <div>
                    <div className={styles.name}>{r.name}</div>
                    <div className={styles.role}>{r.date}</div>
                  </div>
                </div>
              </div>
            ))}
          </div>
        </div>

        <div className={styles.nav}>
          <button
            className={`${styles.navBtn} ${page === 0 ? styles.navBtnDisabled : ''}`}
            onClick={() => scrollTo(Math.max(0, page - 1))}
            aria-label="Назад"
          >←</button>

          <div className={styles.dots}>
            {Array.from({ length: maxPage + 1 }).map((_, i) => (
              <button
                key={i}
                className={`${styles.dot} ${i === page ? styles.dotActive : ''}`}
                onClick={() => scrollTo(i)}
                aria-label={`Страница ${i + 1}`}
              />
            ))}
          </div>

          <button
            className={`${styles.navBtn} ${page === maxPage ? styles.navBtnDisabled : ''}`}
            onClick={() => scrollTo(Math.min(maxPage, page + 1))}
            aria-label="Вперёд"
          >→</button>
        </div>
      </div>
    </section>
  )
}
